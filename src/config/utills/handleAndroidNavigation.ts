export function handleArabicNavigation(number: string) {
    if (number == 6)
    {
        return 0
    }
    else if (number == 5)
    {
        return 1
    }
    else if (number == 4)
    {
        return 2
    }
    else if (number == 3)
    {
        return 3
    }
    else if (number == 2)
    {
        return 4
    }
    else if (number == 1)
    {
        return 5
    }
    else if (number == 0)
    {
        return 6
    }
  }
